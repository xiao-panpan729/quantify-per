def build_mapping():
    """构建板块→叙事链映射"""
    # 读取通达信 270 个概念板块
    gn_path = Path("C:/zd_cjzq/T0002/hq_cache/block_gn.dat")
    reader = BlockReader()
    df = reader.get_df(str(gn_path), 0)
    tdx_sectors = sorted(df["blockname"].unique())

    print(f"通达信概念板块数: {len(tdx_sectors)}")

    # 构建映射
    # { "板块名": [{"chain": "链名", "id": "#11", "grade": "S"}, ...] }
    sector_map = {}

    for sector in tdx_sectors:
        matches = []
        for chain_name, chain_id, grade, keywords in NARRATIVE_RULES:
            for kw in keywords:
                if kw in sector or sector in kw:
                    matches.append({
                        "chain": chain_name,
                        "id": chain_id,
                        "grade": grade
                    })
                    break

        if matches:
            sector_map[sector] = matches
        elif sector in UNCOVERED_SECTORS:
            # 品牌/公司类——未覆盖，需研报精读定级
            sector_map[sector] = [{"chain": "未覆盖", "id": "--", "grade": "U"}]
        else:
            # 无映射
            sector_map[sector] = []

    # 统计
    mapped = sum(1 for v in sector_map.values() if v and v[0].get("grade") != "U")
    uncovered = sum(1 for v in sector_map.values() if v and v[0].get("grade") == "U")
    unmapped = sum(1 for v in sector_map.values() if not v)

    print(f"有映射: {mapped} 个板块")
    print(f"未覆盖: {uncovered} 个板块（品牌/公司类，需研报定级）")
    print(f"无映射: {unmapped} 个板块（纯交易概念）")

    # 输出无映射的板块，便于补全
    print("\n=== 无映射板块 ===")
    for sector, matches in sorted(sector_map.items()):
        if not matches:
            print(f"  {sector}")

    return sector_map


def main():
    sector_map = build_mapping()

    # 写入 narratives/
    output_dir = Path("narratives")
    output_dir.mkdir(parents=True, exist_ok=True)
    output_path = output_dir / "tdx_sector_narrative_map.json"

    with open(output_path, "w", encoding="utf-8") as f:
        json.dump(sector_map, f, ensure_ascii=False, indent=2)

    print(f"\n已写入: {output_path}")

    # 写入结构化的链→板块反向表
    chain_to_sectors = {}
    for sector, chains in sector_map.items():
        for c in chains:
            key = f"{c['grade']}|{c['id']}|{c['chain']}"
            chain_to_sectors.setdefault(key, []).append(sector)

    reverse_path = output_dir / "chain_tdx_sectors.json"
    with open(reverse_path, "w", encoding="utf-8") as f:
        json.dump(chain_to_sectors, f, ensure_ascii=False, indent=2)
    print(f"已写入: {reverse_path}")

    print("\n=== 各等级板块覆盖 ===")
    by_grade = {"S": 0, "A": 0, "B": 0, "C": 0}
    for sector, chains in sector_map.items():
        used = set()
        for c in chains:
            if c["grade"] in by_grade and c["grade"] not in used:
                by_grade[c["grade"]] += 1
                used.add(c["grade"])
    for g in ["S", "A", "B", "C"]:
        print(f"  {g}级: {by_grade[g]} 个板块")


if __name__ == "__main__":
    main()
